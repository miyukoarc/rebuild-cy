<template>
  <div class="app-container">
    <el-card class="content-spacing">
      <tool-bar :hasExport="false" :hasImport="false">
        <div slot="right">
          <el-t-button
            size="small"
            :popAuth="true"
            type='primary'
            :auth="permissionMap['department']['department_add']"
            @click="handleCreate"
          >创建部门</el-t-button>
        </div>
      </tool-bar>
    </el-card>

    <el-card class="content-spacing">
      <el-table
        v-loading="loading"
        :data="departmentList"
        style="width: 100%"
        row-key="uuid"
        lazy
        
        default-expand-all
        @row-click="handleDetail"
        :tree-props="{ children: 'children' }"
       >
        <el-table-column  label="名称" align="left">
            <template v-slot="{row}">
                {{row.name}}
            </template>
        </el-table-column>
        <el-table-column label="标签">
            <template v-slot="{row}">
            
                <span>{{row.orgNode?'组织':'部门'}}</span>
            </template>
        </el-table-column>
        <el-table-column label="操作" align="left" width="240">
          <template slot-scope="scope">
            <el-t-button
              type="text"
              size="mini"
              :auth="permissionMap['department']['department_update']"
              :popAuth="true"
              @click.stop="handleEdit(scope.$index,scope.row)"
            >编辑</el-t-button>
            <el-divider direction="vertical"></el-divider>
            <el-t-button
              type="text"
              :auth="permissionMap['department']['department_delete']"
              :popAuth="true"
              size="mini"
              @click.stop="handleDelete(scope.row)"
            >删除</el-t-button>
          </template>
        </el-table-column>
      </el-table>
    </el-card>

    <!-- <el-card class="content-spacing">
        <Cascader :options="departmentList" :props="{childre:'children',department:'nodes'}"></Cascader>
    </el-card> -->

    <form-dialog ref="formDialog">

    </form-dialog>
  </div>
</template>

<script>
// import mHeadedr from "./header";
import UserDetail from './detail.vue'
import ListHeader from './header.vue'
import FormDialog from './dialog'
import ToolBar from './tool-bar'
import Cascader from '@/components/Cascader'

import { mapState, mapMutations, mapActions } from 'vuex'

export default {
  components: {
    ListHeader,
    UserDetail,
    FormDialog,
    ToolBar,
    Cascader
  },
  data() {
    return {}
  },
  watch: {},
  computed: {
    ...mapState({
      departmentList: state => state.department.departmentList,
      loading: state => state.department.loading,

      permissionMap: state => state.permission.permissionMap
    }),
    routesData() {
      return this.routes
    }
  },
  created() {
    this.initDataList()
    // this.initFilter()
  },
  mounted() {
    this.$bus.$on('showFormDialog', target => {
      this.$refs['formDialog'].event = 'CreateTemplate'
      this.$refs['formDialog'].eventType = 'create'
      this.$refs['formDialog'].dialogVisible = true
    })
  },
  beforeDestroy() {
    this.$bus.$off('showFormDialog')
  },
  methods: {
    handleClick(val, e) {
      e.stopPropagation()
      this.handleDelete(val.uuid)
      alert('点击')
    },

    handleRowClick(value) {
      this.$store.commit('department/SAVE_DETAIL', value)
      this.$refs['formDialog'].event = 'EditTemplate'
      this.$refs['formDialog'].eventType = 'edit'
      this.$refs['formDialog'].dialogVisible = true
    },
    sortChange(val) {
      this.initDataList()
    },
    pageChange() {
      this.initDataList()
    },
    initFilter() {
      this.$store
        .dispatch('department/getDepartmentListSelect')
        .then(() => {})
        .catch(err => {
          this.$message({
            type: 'error',
            message: err
          })
        })
    },
    initDataList() {
      this.$store
        .dispatch('department/getDepartmentListAll')
        .then(() => {})
        .catch(err => {
          this.$message({
            type: 'error',
            message: err
          })
        })
    },
    handleEdit(index, row) {
    //   const payload = this.departmentList[index]
    //   console.log(row)
        this.$store.commit('department/SAVE_DETAIL', row)
      this.$refs['formDialog'].event = 'EditTemplate'
      this.$refs['formDialog'].eventType = 'edit'
      this.$refs['formDialog'].dialogVisible = true
    },
    handleDelete(val) {
      console.log(val.uuid)
      const uuid = val.uuid

      const payload = {
          uuid: uuid
      }
      
      this.$confirm('是否删除当前部门', 'Warning', {
        confirmButtonText: '确定',
        cancelButtonText: '取消',
        type: 'warning'
      })
        .then(async () => {
          await this.$store
            .dispatch('department/deleteDepartment', payload)
            .then(() => {
              this.$message({
                type: 'success',
                message: '操作成功'
              })
              this.initDataList()
            })
            .catch(err => {
              this.$message({
                type: 'error',
                message: err
              })
            })
        })
        .catch(err => {
          
        })
    },
    handleDetail() {},
    handleCreate() {
      this.$refs['formDialog'].event = 'CreateTemplate'
      this.$refs['formDialog'].eventType = 'create'
      this.$refs['formDialog'].dialogVisible = true
    }
  }
}
</script>

